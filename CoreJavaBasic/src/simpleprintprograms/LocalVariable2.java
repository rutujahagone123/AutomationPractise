package simpleprintprograms;

public class LocalVariable2 {

	public static void main(String[] args) {
		int age=30;
		int salary=3000;//local variable
		System.out.println("age:"+age);
		System.out.println("salary:"+salary);
		

	}

}
/**
* Local Variable: is a variable declared inside method body or passed as an argument is known as local variable.
* local variable has to initialized before we use them,otherwise java compiler will give an error.
**/