package loops;

public class loop5 {

	public static void main(String[] args) {
		for(char c='a';c<='z';c++) {
			System.out.println(c+"  ");
			
		}
		for(int i=1;i<=25;i++) {
			System.out.println(i+" ");
		}
	for(int i1=25;i1>=1;i1--) {
		System.out.println(i1+" ");
	}
	}
}
