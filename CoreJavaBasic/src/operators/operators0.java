package operators;

public class operators0 {
			// relational operators
	public static void main(String[] args) {
int p=15, q=25;
//		equals to operator
System.out.println(p==q);
//		not equals to
System.out.println(p!= q);
//		greater than
System.out.println(p>q);
System.out.println(p>=q);
System.out.println(p<q);
System.out.println(p<=q);
	}

}
